import java.util.Scanner;

public class Switch {
	
	public static void main(String args[])
	{
		Scanner  s = new Scanner(System.in);
		
		int n = s.nextInt();
		
		switch(n)
		{
		case 1 :
			System.out.println("test");
			break;
			
		case 2 :
		System.out.println("teshvhhjb hj");
		break;
		
		default :
			
			System.out.println("So,e otehr no");
		}
	}

}
